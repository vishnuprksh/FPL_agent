import sqlite3
from fpl_agent.database_connection import get_connection

def init_db():
    """Initialize the database and create tables if they don't exist."""
    conn = get_connection()
    c = conn.cursor()
    c.execute('''CREATE TABLE IF NOT EXISTS teams (
                    id INTEGER PRIMARY KEY,
                    code INTEGER,
                    draw INTEGER,
                    form REAL,
                    loss INTEGER,
                    name TEXT,
                    played INTEGER,
                    points INTEGER,
                    position INTEGER,
                    short_name TEXT,
                    strength INTEGER,
                    team_division INTEGER,
                    unavailable BOOLEAN,
                    win INTEGER,
                    strength_overall_home INTEGER,
                    strength_overall_away INTEGER,
                    strength_attack_home INTEGER,
                    strength_attack_away INTEGER,
                    strength_defence_home INTEGER,
                    strength_defence_away INTEGER,
                    pulse_id INTEGER
                )''')
    c.execute('''CREATE TABLE IF NOT EXISTS players (
                    assists INTEGER,
                    birth_date TEXT,
                    bonus INTEGER,
                    bps INTEGER,
                    can_select BOOLEAN,
                    can_transact BOOLEAN,
                    chance_of_playing_next_round REAL,
                    chance_of_playing_this_round REAL,
                    clean_sheets INTEGER,
                    clean_sheets_per_90 TEXT,
                    clearances_blocks_interceptions INTEGER,
                    player_code INTEGER UNIQUE,
                    corners_and_indirect_freekicks_order INTEGER,
                    corners_and_indirect_freekicks_text TEXT,
                    cost_change_event INTEGER,
                    cost_change_event_fall INTEGER,
                    cost_change_start INTEGER,
                    cost_change_start_fall INTEGER,
                    creativity TEXT,
                    creativity_rank INTEGER,
                    creativity_rank_type INTEGER,
                    defensive_contribution INTEGER,
                    defensive_contribution_per_90 TEXT,
                    direct_freekicks_order INTEGER,
                    direct_freekicks_text TEXT,
                    dreamteam_count INTEGER,
                    element_type INTEGER,
                    ep_next TEXT,
                    ep_this TEXT,
                    event_points INTEGER,
                    expected_assists TEXT,
                    expected_assists_per_90 TEXT,
                    expected_goal_involvements TEXT,
                    expected_goal_involvements_per_90 TEXT,
                    expected_goals TEXT,
                    expected_goals_conceded TEXT,
                    expected_goals_conceded_per_90 TEXT,
                    expected_goals_per_90 TEXT,
                    first_name TEXT,
                    form TEXT,
                    form_rank INTEGER,
                    form_rank_type INTEGER,
                    goals_conceded INTEGER,
                    goals_conceded_per_90 TEXT,
                    goals_scored INTEGER,
                    has_temporary_code BOOLEAN,
                    ict_index TEXT,
                    ict_index_rank INTEGER,
                    ict_index_rank_type INTEGER,
                    season_id INTEGER PRIMARY KEY,
                    in_dreamteam BOOLEAN,
                    influence TEXT,
                    influence_rank INTEGER,
                    influence_rank_type INTEGER,
                    minutes INTEGER,
                    news TEXT,
                    news_added TEXT,
                    now_cost INTEGER,
                    now_cost_rank INTEGER,
                    now_cost_rank_type INTEGER,
                    opta_code INTEGER,
                    own_goals INTEGER,
                    penalties_missed INTEGER,
                    penalties_order INTEGER,
                    penalties_saved INTEGER,
                    penalties_text TEXT,
                    photo TEXT,
                    points_per_game TEXT,
                    points_per_game_rank INTEGER,
                    points_per_game_rank_type INTEGER,
                    recoveries INTEGER,
                    red_cards INTEGER,
                    region INTEGER,
                    removed BOOLEAN,
                    saves INTEGER,
                    saves_per_90 TEXT,
                    second_name TEXT,
                    selected_by_percent TEXT,
                    selected_rank INTEGER,
                    selected_rank_type INTEGER,
                    special BOOLEAN,
                    squad_number INTEGER,
                    starts INTEGER,
                    starts_per_90 TEXT,
                    status TEXT,
                    tackles INTEGER,
                    team INTEGER,
                    team_code INTEGER,
                    team_join_date TEXT,
                    threat TEXT,
                    threat_rank INTEGER,
                    threat_rank_type INTEGER,
                    total_points INTEGER,
                    transfers_in INTEGER,
                    transfers_in_event INTEGER,
                    transfers_out INTEGER,
                    transfers_out_event INTEGER,
                    value_form TEXT,
                    value_season TEXT,
                    web_name TEXT,
                    yellow_cards INTEGER,
                    season TEXT
                )''')
    c.execute('''CREATE TABLE IF NOT EXISTS player_history (
                    season_id INTEGER,
                    player_code INTEGER,
                    round INTEGER,
                    total_points INTEGER,
                    opponent_team INTEGER,
                    season TEXT,
                    assists INTEGER,
                    bonus INTEGER,
                    bps INTEGER,
                    clean_sheets INTEGER,
                    creativity REAL,
                    goals_conceded INTEGER,
                    goals_scored INTEGER,
                    ict_index REAL,
                    influence REAL,
                    minutes INTEGER,
                    own_goals INTEGER,
                    penalties_missed INTEGER,
                    penalties_saved INTEGER,
                    red_cards INTEGER,
                    saves INTEGER,
                    threat REAL,
                    value INTEGER,
                    was_home BOOLEAN,
                    yellow_cards INTEGER,
                    fixture INTEGER,
                    xP REAL,
                    expected_assists REAL,
                    expected_goal_involvements REAL,
                    expected_goals REAL,
                    expected_goals_conceded REAL,
                    kickoff_time TEXT,
                    modified BOOLEAN,
                    selected INTEGER,
                    starts INTEGER,
                    team_a_score INTEGER,
                    team_h_score INTEGER,
                    transfers_balance INTEGER,
                    transfers_in INTEGER,
                    transfers_out INTEGER,
                    clearances_blocks_interceptions INTEGER,
                    defensive_contribution INTEGER,
                    recoveries INTEGER,
                    tackles INTEGER,
                    fixture_difficulty INTEGER,
                    FOREIGN KEY(season_id, season) REFERENCES players(season_id, season),
                    FOREIGN KEY(player_code) REFERENCES players(player_code)
                )''')
    c.execute('''CREATE TABLE IF NOT EXISTS fixtures (
                    id INTEGER,
                    code INTEGER,
                    event INTEGER,
                    finished BOOLEAN,
                    finished_provisional BOOLEAN,
                    kickoff_time TEXT,
                    minutes INTEGER,
                    provisional_start_time TEXT,
                    started BOOLEAN,
                    team_a INTEGER,
                    team_a_score INTEGER,
                    team_h INTEGER,
                    team_h_score INTEGER,
                    team_h_difficulty INTEGER,
                    team_a_difficulty INTEGER,
                    pulse_id INTEGER,
                    season TEXT,
                    PRIMARY KEY (id, season)
                )''')
    conn.commit()
    conn.close()
